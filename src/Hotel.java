public class Hotel {
    private String nomeHotel;
    private String cnpj;

    public String getNome() {
        return nomeHotel;
    }

    public void setNome(String nome) {
        this.nomeHotel = nome;
    }

    public String getCnpj() {
        return cnpj;
    }

    public void setCnpj(String cnpj) {
        this.cnpj = cnpj;
    }
}
